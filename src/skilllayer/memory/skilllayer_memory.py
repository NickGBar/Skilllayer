# Frontmatter parser / writer (no PyYAML)
# ---------------------------------------------------------------------------

def parse_frontmatter(text: str) -> tuple[dict[str, Any], str]:
    """Return (meta, body) from markdown with YAML-like frontmatter.

    Supported value types: null, bool, int, simple list, string.
    """
    if not text.startswith("---"):
        return {}, text
    try:
        end = text.index("---", 3)
    except ValueError:
        return {}, text
    yaml_block = text[3:end].strip()
    body = text[end + 3:].lstrip("\n")
    meta: dict[str, Any] = {}
    for raw_line in yaml_block.splitlines():
        line = raw_line.strip()
        if not line or not line.startswith("#") is False or ":" not in line:
            if ":" not in line:
                continue
        key, _, raw_val = line.partition(":")
        key = key.strip()
        val = raw_val.strip()
        if val in ("null", "~", ""):
            meta[key] = None
        elif val == "true":
            meta[key] = True
        elif val == "false":
            meta[key] = False
        elif re.fullmatch(r"[1-9][0-9]*|0", val):
            meta[key] = int(val)
        elif val.startswith("[") and val.endswith("]"):
            inner = val[1:-1]
            meta[key] = [v.strip().strip("'\"") for v in inner.split(",") if v.strip()]
        else:
            meta[key] = val
    return meta, body


def write_frontmatter(meta: dict[str, Any], body: str) -> str:
    """Serialize meta dict + body into frontmatter markdown."""
    lines = ["---"]
    for k, v in meta.items():
        if v is None:
            lines.append(f"{k}: null")
        elif isinstance(v, bool):
            lines.append(f"{k}: {'true' if v else 'false'}")
        elif isinstance(v, list):
            inner = ", ".join(str(item) for item in v)
            lines.append(f"{k}: [{inner}]")
        else:
            lines.append(f"{k}: {v}")
    lines.append("---")
    return "\n".join(lines) + "\n\n" + body.rstrip("\n") + "\n"


