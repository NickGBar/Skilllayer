class MemoryStateCorruptError(Exception):
    """Raised by _read_state when .state.json EXISTS but cannot be parsed as
    a JSON object.

    Deliberately distinct from "file does not exist yet" (a normal, safe
    default for a brand-new store). Previously, a corrupt-but-present file
    silently fell back to {"next_decision_id": 1}, and the very next
    claim_next_id() call would persist that reset counter back to disk —
    risking a duplicate id that collides with an already-existing decision or
    todo. Callers must catch this and refuse to claim/persist a reset counter
    rather than silently trusting the fallback as new source of truth. The
    original corrupt file is left on disk untouched, as the only evidence of
    what went wrong.
    """


def _read_state(skilllayer_dir: Path) -> dict[str, Any]:
    state_file = skilllayer_dir / ".state.json"
    if not state_file.exists():
        return {"next_decision_id": 1}
    try:
        data = json.loads(state_file.read_text(encoding="utf-8"))
    except (json.JSONDecodeError, OSError) as exc:
        raise MemoryStateCorruptError(f".state.json exists but is not valid JSON: {exc}") from exc
    if not isinstance(data, dict):
        raise MemoryStateCorruptError(".state.json exists but does not contain a JSON object.")
    return data


def _write_state(skilllayer_dir: Path, state: dict[str, Any]) -> None:
    atomic_write_json(skilllayer_dir / ".state.json", state)


def claim_next_id(skilllayer_dir: Path, counter_key: str) -> str:
    """Claim and persist the next zero-padded 4-digit id for the given counter.

    Generalized from the original decision-only claim_next_decision_id so
    CrossSessionTodoWorkflow's todo ids can share the same collision-free
    counter mechanism in .state.json (via a separate "next_todo_id" key)
    instead of a second, independently-implemented one.

    The read-modify-write is performed under the whole-store memory_lock, so
    two concurrent processes can never claim the same id. Reentrant: safe to
    call standalone, or nested inside a caller's own already-held
    memory_lock() (e.g. add_todo's larger transaction).

    Raises MemoryStateCorruptError if .state.json exists but is unreadable —
    never silently resets the counter and persists that reset.
    """
    with memory_lock(skilllayer_dir):
        state = _read_state(skilllayer_dir)
        n = int(state.get(counter_key, 1))
        state[counter_key] = n + 1
        _write_state(skilllayer_dir, state)
        return f"{n:04d}"


