def extract_section_body(body: str, heading: str) -> str:
    """Extract the text content under a markdown ## heading.

    Public (not module-private) because runner/core.py's
    CompareContextSnapshotsWorkflow reuses it to pull State/Open Questions
    out of two parsed snapshots, rather than re-implementing this parsing.
    """
    heading_pat = re.compile(r"^#{1,6}\s+" + re.escape(heading) + r"\s*$")
    lines = body.splitlines()
    in_section = False
    result: list[str] = []
    for line in lines:
        if heading_pat.match(line):
            in_section = True
            continue
        if in_section:
            if re.match(r"^#{1,6}\s+", line):
                break
            result.append(line)
    return "\n".join(result).strip()


# ---------------------------------------------------------------------------
# NeedsWrap — "you did work, you never saved it"
#
# Automatic, read-only check surfaced in INDEX.md by regenerate_index() and
# recomputed live by rehydrate_context(). Reuses two files that already exist —
# context/latest.md (last save time) and file_watch_snapshot.json (per-file
# mtimes from the LAST WatchFileChanges run). It NEVER triggers a new scan and
# never calls WatchFileChangesWorkflow; it only reads what is already on disk.
# ---------------------------------------------------------------------------

NEEDS_WRAP_SAMPLE_CAP = 5


def _parse_iso(ts: Any) -> datetime | None:
    """Tolerant ISO-8601 parse -> aware UTC datetime, or None.

    Handles both timestamp formats in play without assuming either:
      - latest.md 'created':        '2026-07-07T14:22:05Z'
      - snapshot recorded_at/mtime: '2026-07-07T14:22:05.123456+00:00'
    Python 3.10's datetime.fromisoformat() rejects a trailing 'Z', so it is
    normalized to '+00:00' first. Naive results are assumed UTC.
    """
    if not isinstance(ts, str) or not ts.strip():
        return None
    text = ts.strip()
    if text.endswith("Z"):
        text = text[:-1] + "+00:00"
    try:
        dt = datetime.fromisoformat(text)
    except ValueError:
        return None
    if dt.tzinfo is None:
        dt = dt.replace(tzinfo=timezone.utc)
    return dt.astimezone(timezone.utc)


def _recent_watch_paths(files: dict[str, Any], cap: int) -> list[str]:
    """Return up to `cap` watched paths, most-recently-modified first."""
    _floor = datetime.min.replace(tzinfo=timezone.utc)
    items: list[tuple[str, datetime]] = []
    for rel, info in files.items():
        if not isinstance(info, dict):
            continue
        items.append((rel, _parse_iso(info.get("mtime")) or _floor))
    items.sort(key=lambda pair: (pair[1], pair[0]), reverse=True)
    return [rel for rel, _ in items[:cap]]


def compute_needs_wrap(skilllayer_dir: Path) -> dict[str, Any]:
    """Detect real file changes since the last saved context snapshot.

    Pure read of context/latest.md and file_watch_snapshot.json. Returns a
    structured dict; regenerate_index() renders it into INDEX.md and
    rehydrate_context() recomputes it live. status is one of:
      unknown      — no file-watch snapshot exists yet (cannot tell; not "clean")
      never_saved  — file-watch data exists but no context snapshot ever saved
      stale_watch  — the last file-watch predates the last snapshot
      needs_wrap   — files were modified on disk since the last snapshot
      clean        — nothing changed since the last snapshot (as of last watch)
    """
    result: dict[str, Any] = {
        "status": "unknown",
        "changed_count": 0,
        "last_snapshot": None,
        "last_file_watch": None,
        "changed_sample": [],
    }

    # Last save time (context/latest.md 'created')
    latest = skilllayer_dir / "context" / "latest.md"
    save_iso: str | None = None
    save_dt: datetime | None = None
    if latest.exists():
        try:
            meta, _ = parse_frontmatter(latest.read_text(encoding="utf-8"))
            created = meta.get("created")
            if isinstance(created, str):
                save_iso = created
                save_dt = _parse_iso(created)
        except OSError:
            save_iso, save_dt = None, None
    result["last_snapshot"] = save_iso

    # Last file-watch snapshot (never re-scan — read what WatchFileChanges left)
    snap_path = skilllayer_dir / "file_watch_snapshot.json"
    watch: dict[str, Any] | None = None
    if snap_path.exists():
        try:
            loaded = json.loads(snap_path.read_text(encoding="utf-8"))
            watch = loaded if isinstance(loaded, dict) else None
        except (json.JSONDecodeError, OSError):
            watch = None

    if watch is None:
        result["status"] = "unknown"
        return result

    recorded_iso = watch.get("recorded_at")
    result["last_file_watch"] = recorded_iso if isinstance(recorded_iso, str) else None
    recorded_dt = _parse_iso(recorded_iso)
    files = watch.get("files")
    if not isinstance(files, dict):
        files = {}

    if save_dt is None:
        # File-watch data exists but no context snapshot has ever been saved.
        result["status"] = "never_saved"
        result["changed_count"] = len(files)
        result["changed_sample"] = _recent_watch_paths(files, NEEDS_WRAP_SAMPLE_CAP)
        return result

    if recorded_dt is not None and recorded_dt < save_dt:
        # The watch predates the save; its mtimes cannot speak to post-save work.
        result["status"] = "stale_watch"
        return result

    changed: list[tuple[str, datetime]] = []
    for rel, info in files.items():
        if not isinstance(info, dict):
            continue
        m_dt = _parse_iso(info.get("mtime"))
        if m_dt is not None and m_dt > save_dt:
            changed.append((rel, m_dt))
    changed.sort(key=lambda pair: (pair[1], pair[0]), reverse=True)
    result["changed_count"] = len(changed)
    result["changed_sample"] = [rel for rel, _ in changed[:NEEDS_WRAP_SAMPLE_CAP]]
    result["status"] = "needs_wrap" if changed else "clean"
    return result


def _render_needs_wrap_section(nw: dict[str, Any]) -> str:
    """Render a NeedsWrap result as an INDEX.md section (heading conventions
    match Todos/Decisions: '## <Title> · <Source> — <count/status>')."""
    status = nw.get("status")
    save_date = (nw.get("last_snapshot") or "")[:10]
    watch_at = nw.get("last_file_watch") or "never"
    sample = nw.get("changed_sample") or []
    count = nw.get("changed_count", 0)

    if status == "needs_wrap":
        lines = [
            f"## Unsaved Work · NeedsWrap — {count} files changed since last snapshot",
            f"last snapshot: {save_date or 'never'} · last file-watch: {watch_at}",
            f"{count} file(s) modified on disk after the last saved context snapshot.",
        ]
        if sample:
            lines.append(f"recent: {', '.join(sample)}")
        lines.append("→ Save a context snapshot to consolidate this work.")
        return "\n".join(lines)

    if status == "never_saved":
        lines = [
            "## Unsaved Work · NeedsWrap — no snapshot ever saved",
            f"last file-watch: {watch_at}",
            f"{count} file(s) tracked on disk but no context snapshot has ever been saved.",
        ]
        if sample:
            lines.append(f"recent: {', '.join(sample)}")
        lines.append("→ Save a context snapshot to consolidate this work.")
        return "\n".join(lines)

    if status == "stale_watch":
        return (
            "## Unsaved Work · NeedsWrap — file-watch is stale\n"
            f"last snapshot: {save_date or 'never'} · last file-watch: {watch_at}\n"
            "The last file-watch predates your last snapshot. "
            "Re-run WatchFileChanges to check for newer work."
        )

    if status == "clean":
        return (
            "## Unsaved Work · NeedsWrap — clean\n"
            f"last snapshot: {save_date or 'never'} · last file-watch: {watch_at}\n"
            "No files changed since the last snapshot (as of the last file-watch)."
        )

    # unknown
    return (
        "## Unsaved Work · NeedsWrap — unknown\n"
        "No file-watch data yet. Run WatchFileChanges to enable unsaved-work detection."
    )


def regenerate_index(skilllayer_dir: Path) -> str:
    """Rebuild INDEX.md from context/latest.md, decisions/*.md, preferences.md."""
    sections: list[str] = []

    # --- Context ---
    latest = skilllayer_dir / "context" / "latest.md"
    if latest.exists():
        ctx_meta, ctx_body = parse_frontmatter(latest.read_text(encoding="utf-8"))
        created = str(ctx_meta.get("created", _utc_date()))[:10]
        state_text = extract_section_body(ctx_body, "State")
        state_first = (state_text.splitlines()[0] if state_text else "(no state recorded)").strip()
        oq_text = extract_section_body(ctx_body, "Open Questions")
        oqs = [ln.lstrip("- ").strip() for ln in oq_text.splitlines() if ln.strip().startswith("-")]
        oq_block = "\n".join(f"  - {q}" for q in oqs) if oqs else "  (none)"
        sections.append(
            f"## Context · SaveContextSnapshot\n"
            f"snapshot: context/latest.md · {created}\n"
            f"state: {state_first}\n"
            f"open_questions: {len(oqs)}\n"
            f"{oq_block}"
        )

    # --- Decisions ---
    decisions_dir = skilllayer_dir / "decisions"
    all_files = sorted(decisions_dir.glob("*.md")) if decisions_dir.exists() else []
    total = len(all_files)
    last_three = all_files[-3:]
    d_lines: list[str] = []
    for df in reversed(last_three):
        d_meta, _ = parse_frontmatter(df.read_text(encoding="utf-8"))
        d_id = str(d_meta.get("id", df.stem[:4]))
        raw_stem = df.stem
        title_part = raw_stem[5:].replace("-", " ") if len(raw_stem) > 5 else raw_stem
        d_status = str(d_meta.get("status", "proposed"))
        d_created = str(d_meta.get("created", ""))[:10]
        d_lines.append(f"- {d_id} {title_part} · {d_status} · {d_created}")
    d_block = "\n".join(d_lines) if d_lines else "  (none)"
    sections.append(
        f"## Decisions · TrackDecisionLog — {total} total, last 3\n"
        f"{d_block}\n"
        f"full log: decisions/"
    )

    # --- Preferences ---
    prefs_file = skilllayer_dir / "preferences.md"
    if prefs_file.exists():
        _, prefs_body = parse_frontmatter(prefs_file.read_text(encoding="utf-8"))
        digest_lines: list[str] = []
        current_domain: str | None = None
        added_per_domain: set[str] = set()
        for line in prefs_body.splitlines():
            stripped = line.strip()
            if stripped.startswith("## "):
                current_domain = stripped[3:].strip().lower()
                added_per_domain = set()
            elif stripped.startswith("- ") and current_domain:
                key_val = stripped[2:].strip()
                if current_domain not in added_per_domain:
                    digest_lines.append(f"- {current_domain}: {key_val}")
                    added_per_domain.add(current_domain)
        digest = "\n".join(digest_lines) if digest_lines else "  (none)"
        sections.append(
            f"## Preferences · RememberUserPreferences\n"
            f"{digest}\n"
            f"full: preferences.md"
        )

    # --- Todos ---
    todos_file = skilllayer_dir / TODOS_FILENAME
    if todos_file.exists():
        try:
            todos_data = json.loads(todos_file.read_text(encoding="utf-8"))
            all_todos = todos_data.get("todos", []) if isinstance(todos_data, dict) else []
        except (json.JSONDecodeError, OSError):
            all_todos = []
        open_todos = [t for t in all_todos if isinstance(t, dict) and t.get("status") == "open"]
        # All open todos, not a "last N" digest — unlike Decisions, the whole
        # point is showing everything still outstanding. The cap is a pure
        # runaway guard, not a deliberate truncation.
        shown = open_todos[:TODO_INDEX_DISPLAY_CAP]
        t_lines = [f"- {t['id']} {t['text']}" for t in shown]
        if len(open_todos) > TODO_INDEX_DISPLAY_CAP:
            t_lines.append(f"  ... and {len(open_todos) - TODO_INDEX_DISPLAY_CAP} more open todo(s)")
        t_block = "\n".join(t_lines) if t_lines else "  (none)"
        sections.append(
            f"## Todos · CrossSessionTodo — {len(open_todos)} open\n"
            f"{t_block}\n"
            f"full list: {TODOS_FILENAME}"
        )

    # --- Unsaved work (NeedsWrap) ---
    # Only surface when there is something to reason about (a context snapshot
    # or file-watch data exists), matching how the other sections appear only
    # when their source exists — avoids noise on a brand-new empty store.
    snap_path = skilllayer_dir / "file_watch_snapshot.json"
    if latest.exists() or snap_path.exists():
        sections.append(_render_needs_wrap_section(compute_needs_wrap(skilllayer_dir)))

    meta = {"schema": SCHEMA_VERSION, "updated": _utc_iso()}
    return write_frontmatter(meta, "\n\n".join(sections))


def write_index(skilllayer_dir: Path) -> None:
    """Write the regenerated INDEX.md to disk.

    Locked and atomic. Reentrant: safe standalone, or nested inside a
    caller's larger already-locked transaction (every write workflow calls
    this as its final step) — regenerating and writing the index happens
    under the SAME lock as the rest of that transaction, so INDEX.md is never
    regenerated from a partially-updated store.
    """
    with memory_lock(skilllayer_dir):
        atomic_write_text(skilllayer_dir / "INDEX.md", regenerate_index(skilllayer_dir))


