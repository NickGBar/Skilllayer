def _utc_iso() -> str:
    """ISO-8601 timestamp for frontmatter fields."""
    return datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")


def _utc_date() -> str:
    return datetime.now(timezone.utc).strftime("%Y-%m-%d")


def _utc_filename_ts() -> str:
    """Compact timestamp safe for filenames.

    Microsecond resolution (not just seconds): under the memory_lock, two
    concurrent save_context_snapshot rotations are serialized, but a
    second-only timestamp could still collide if both land in the same
    wall-clock second, silently overwriting one rotated history file with
    the other. Microseconds make that collision negligible in practice.
    """
    return datetime.now(timezone.utc).strftime("%Y%m%dT%H%M%S%f") + "Z"


# ---------------------------------------------------------------------------
# Cross-process lock over the whole .skilllayer memory transaction boundary
#
# One lock, not one per file: every read-modify-write sequence that touches
# related memory files (e.g. AddTodo's todos.json + .state.json counter +
# INDEX.md) acquires this ONE lock for its full logical transaction, so two
# concurrent processes can never interleave partial updates.
#
# Built on fcntl.flock (POSIX only — this module makes no claim of
# cross-process safety on Windows; MEMORY_LOCK_CROSS_PROCESS_SUPPORTED
# reflects this truthfully rather than silently pretending otherwise). flock
# is scoped to the OS-level open-file-description and is released
# automatically by the kernel the instant the holding process exits for ANY
# reason (normal return, crash, kill -9) — this eliminates the classic
# "stale PID lock file" problem by construction: the lock can never outlive
# its holder, so there is no stale-lock cleanup to implement.
# ---------------------------------------------------------------------------

MEMORY_LOCK_CROSS_PROCESS_SUPPORTED = fcntl is not None
MEMORY_LOCK_FILENAME = ".memory.lock"
DEFAULT_MEMORY_LOCK_TIMEOUT_SECONDS = 10.0
_MEMORY_LOCK_POLL_INTERVAL_SECONDS = 0.05


class MemoryLockTimeoutError(Exception):
    """Raised when the cross-process .skilllayer memory lock could not be
    acquired within the timeout — another process is actively holding it.

    NOT a stale-lock condition: flock is released by the kernel the instant
    its holder's process exits, for any reason including a crash, so a
    timeout here means genuine live contention (a slow or hung concurrent
    writer), never an orphaned leftover lock.
    """

    def __init__(self, skilllayer_dir: Path, timeout: float) -> None:
        self.skilllayer_dir = skilllayer_dir
        self.timeout = timeout
        super().__init__(
            f"Could not acquire the .skilllayer memory lock within {timeout}s "
            f"(another process is actively writing to {skilllayer_dir}). "
            "This is live contention, not a stale lock — the OS releases this "
            "lock automatically the instant its holder exits. Retry the "
            "operation; if it keeps timing out, another process is genuinely "
            "stuck, not merely a leftover lock file."
        )


class _ReentrantLockState(threading.local):
    def __init__(self) -> None:
        self.depth = 0
        self.fd: int | None = None


_LOCK_STATE = _ReentrantLockState()

# Process-local mutual exclusion, separate from the thread-local depth
# bookkeeping above. Two threads in the SAME process racing on memory_lock()
# must never both proceed at once — depth alone cannot provide that, since
# threading.local() gives each thread its own independent depth counter.
# RLock (not Lock) because the reentrant nested-call path above re-enters
# from the SAME thread while already holding it. Keyed by resolved
# .skilllayer directory so two different repositories' locks never
# contend with each other.
_PROCESS_LOCKS_GUARD = threading.Lock()
_PROCESS_LOCKS: dict[Path, threading.RLock] = {}


def _process_lock_for(skilllayer_dir: Path) -> threading.RLock:
    key = skilllayer_dir.resolve()
    with _PROCESS_LOCKS_GUARD:
        lock = _PROCESS_LOCKS.get(key)
        if lock is None:
            lock = threading.RLock()
            _PROCESS_LOCKS[key] = lock
        return lock


@contextlib.contextmanager
def memory_lock(
    skilllayer_dir: Path,
    timeout: float = DEFAULT_MEMORY_LOCK_TIMEOUT_SECONDS,
) -> Iterator[None]:
    """Acquire the whole-store memory lock for one transaction.

    Two layers, acquired in this order (released in reverse):
      1. A process-local threading.RLock, keyed by resolved skilllayer_dir —
         provides real mutual exclusion between THREADS in this process.
         threading.local() depth bookkeeping alone cannot do this: each
         thread has its own independent depth counter, so two threads could
         both see depth==0 and both proceed at once with no serialization.
      2. On POSIX only, fcntl.flock on .memory.lock — provides mutual
         exclusion between PROCESSES. Not attempted on platforms without
         fcntl (see MEMORY_LOCK_CROSS_PROCESS_SUPPORTED); on those platforms
         only same-process/same-thread safety (layer 1) is provided, and
         that fact is not hidden.

    Reentrant within a single thread: a nested memory_lock() call (e.g.
    claim_next_id() invoked from inside add_todo()'s already-held lock) never
    re-flock's — flock is scoped to the open-file-description, not the
    process, so a naive re-open+re-flock from the same thread would
    deadlock against itself. The RLock IS re-acquired on nested calls
    (cheap — natively reentrant for the same thread) purely to keep
    acquire/release symmetric.

    Bounded timeout, shared across both layers (a slow RLock wait eats into
    the time left for the flock wait). Raises MemoryLockTimeoutError if
    either layer is not acquired in time. Release always happens in
    `finally`, including on exception.

    Callers must perform ONLY the transaction's file I/O inside this block —
    no subprocess calls, network access, or other unrelated expensive work.
    """
    skilllayer_dir.mkdir(parents=True, exist_ok=True)
    deadline = time.monotonic() + timeout
    process_lock = _process_lock_for(skilllayer_dir)

    if _LOCK_STATE.depth > 0:
        process_lock.acquire()
        _LOCK_STATE.depth += 1
        try:
            yield
        finally:
            _LOCK_STATE.depth -= 1
            process_lock.release()
        return

    if not process_lock.acquire(timeout=max(deadline - time.monotonic(), 0)):
        raise MemoryLockTimeoutError(skilllayer_dir, timeout)

    try:
        if fcntl is None:
            # No cross-process locking available on this platform. The
            # process_lock above still provides same-process/same-thread
            # mutual exclusion — cross-process safety is NOT claimed here.
            _LOCK_STATE.depth = 1
            try:
                yield
            finally:
                _LOCK_STATE.depth = 0
            return

        lock_path = skilllayer_dir / MEMORY_LOCK_FILENAME
        fd = os.open(str(lock_path), os.O_CREAT | os.O_RDWR, 0o600)
        acquired = False
        try:
            while True:
                try:
                    fcntl.flock(fd, fcntl.LOCK_EX | fcntl.LOCK_NB)
                    acquired = True
                    break
                except BlockingIOError:
                    if time.monotonic() >= deadline:
                        raise MemoryLockTimeoutError(skilllayer_dir, timeout) from None
                    time.sleep(_MEMORY_LOCK_POLL_INTERVAL_SECONDS)
            with contextlib.suppress(OSError):
                os.ftruncate(fd, 0)
                os.write(fd, f"{os.getpid()} {_utc_iso()}\n".encode("utf-8"))
            _LOCK_STATE.depth = 1
            _LOCK_STATE.fd = fd
            try:
                yield
            finally:
                _LOCK_STATE.depth = 0
                _LOCK_STATE.fd = None
        finally:
            if acquired:
                with contextlib.suppress(OSError):
                    fcntl.flock(fd, fcntl.LOCK_UN)
            os.close(fd)
    finally:
        process_lock.release()


# ---------------------------------------------------------------------------
# Atomic write helper — text and JSON, no duplicated safety logic
#
# Write to a temp file in the SAME directory, flush, fsync the file, then
# os.replace() onto the destination (an atomic rename), and best-effort
# fsync the parent directory so the rename is durable across a crash. On any
# failure before the replace, the destination is left completely untouched
# and the temp file is removed — never a truncated or partially-written
# destination file.
# ---------------------------------------------------------------------------

def atomic_write_text(path: Path, text: str, encoding: str = "utf-8") -> None:
    """Atomically write `text` to `path`. Preserves the same encoding and
    universal-newline translation behavior as Path.write_text()."""
    path.parent.mkdir(parents=True, exist_ok=True)
    fd, tmp_name = tempfile.mkstemp(dir=str(path.parent), prefix=f".{path.name}.", suffix=".tmp")
    tmp_path = Path(tmp_name)
    try:
        with os.fdopen(fd, "w", encoding=encoding) as f:
            f.write(text)
            f.flush()
            os.fsync(f.fileno())
        os.replace(tmp_path, path)
    except BaseException:
        with contextlib.suppress(OSError):
            tmp_path.unlink()
        raise
    else:
        with contextlib.suppress(OSError):
            dir_fd = os.open(str(path.parent), os.O_RDONLY)
            try:
                os.fsync(dir_fd)
            finally:
                os.close(dir_fd)


def atomic_write_json(path: Path, data: Any, *, indent: int = 2) -> None:
    """Atomically write `data` as JSON to `path`. See atomic_write_text."""
    atomic_write_text(path, json.dumps(data, indent=indent))


# ---------------------------------------------------------------------------
# Result contract — shared by every user-facing memory workflow result.
#
# status: "healthy" | "warning" | "broken" | "error"
#   healthy — operation completed, no integrity problem. success: true.
#   warning — operation completed, usable result exists, but there is a
#             non-fatal limitation or recoverable condition worth surfacing
#             (e.g. no memory store saved yet). success: true, warnings set.
#   broken  — memory INTEGRITY is invalid; the data must not be trusted as a
#             healthy store (corrupt state/todos file, integrity violation).
#             success: false.
#   error   — the operation could not complete for an environmental or input
#             reason (lock timeout, invalid input, I/O failure) — does not by
#             itself mean the store's data is corrupt. success: false.
#
# Canonical, stable error codes — the SAME spelling everywhere a failure
# surfaces (builder, CLI, direct MCP, generic MCP). Only "broken"-classified
# codes go in _BROKEN_ERROR_CODES; everything else defaults to "error".
# ---------------------------------------------------------------------------

ERROR_MEMORY_LOCK_TIMEOUT = "memory_lock_timeout"
ERROR_MEMORY_STATE_CORRUPT = "memory_state_corrupt"
ERROR_TODOS_STORE_CORRUPT = "todos_store_corrupt"
ERROR_MEMORY_INTEGRITY_BROKEN = "memory_integrity_broken"
ERROR_MEMORY_PERMISSION_DENIED = "memory_permission_denied"
ERROR_MEMORY_IO_ERROR = "memory_io_error"
ERROR_MEMORY_RECORD_NOT_FOUND = "memory_record_not_found"

_BROKEN_ERROR_CODES = frozenset({
    ERROR_MEMORY_STATE_CORRUPT,
    ERROR_TODOS_STORE_CORRUPT,
    ERROR_MEMORY_INTEGRITY_BROKEN,
})


def status_for_error_code(error_code: str | None) -> str:
    """Classify an error_code into the shared status vocabulary.

    error_code is None -> "healthy" by default (callers needing "warning"
    on an otherwise-clean result set status explicitly via finalize_memory_result).
    A code in _BROKEN_ERROR_CODES -> "broken" (data integrity is invalid).
    Any other non-null code -> "error" (operation-level failure, not
    necessarily a corrupt store).
    """
    if error_code is None:
        return "healthy"
    if error_code in _BROKEN_ERROR_CODES:
        return "broken"
    return "error"


def finalize_memory_result(
    result: dict[str, Any],
    *,
    status: str | None = None,
    warnings: list[str] | None = None,
    include_status_field: bool = True,
) -> dict[str, Any]:
    """Stamp success/status/warnings onto a memory-workflow result dict.

    The single place every builder (in this module or in runner/core.py for
    the two read-only workflows that live there) must route its return value
    through, instead of hand-computing `success` at the CLI or MCP layer —
    the reason "success: true" could previously survive a broken/corrupt
    result: each layer was independently guessing at success from whatever
    fields happened to be present.

    status defaults to status_for_error_code(result.get("error_code")); pass
    it explicitly only for a "warning" result whose error_code is legitimately
    None (a healthy-shaped but non-fatal-limited result, e.g. no memory store
    saved yet).

    include_status_field=False for AddTodoWorkflow/MarkTodoDoneWorkflow ONLY:
    both already have a top-level "status" field meaning the TOUCHED TODO's
    own state ("open"/"done"), pre-dating this contract and depended on by
    existing callers/tests/CLI output — stamping the health-status word under
    the same key would silently corrupt that unrelated, already-public field.
    success/error_code/warnings are still fully stamped either way; a caller
    that wants the health-status word for those two can derive it from
    error_code via status_for_error_code().
    """
    error_code = result.get("error_code")
    resolved_status = status or status_for_error_code(error_code)
    if include_status_field:
        result["status"] = resolved_status
    result["success"] = resolved_status in ("healthy", "warning")
    result.setdefault("warnings", list(warnings or []))
    if warnings:
        result["warnings"] = list(warnings)
    return result


