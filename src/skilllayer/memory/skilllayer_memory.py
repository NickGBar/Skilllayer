def _check_index_freshness(skilllayer_dir: Path, index_text: str | None) -> list[dict[str, Any]]:
    if index_text is None:
        return [_finding(
            "index_freshness", "INDEX.md",
            "INDEX.md does not exist yet.",
            "Run any memory write workflow (SaveContextSnapshot, TrackDecisionLog, "
            "RememberUserPreferences, AddTodo) to generate it.",
        )]
    fresh_meta, fresh_body = parse_frontmatter(regenerate_index(skilllayer_dir))
    disk_meta, disk_body = parse_frontmatter(index_text)
    fresh_meta.pop("updated", None)
    disk_meta.pop("updated", None)
    if fresh_meta != disk_meta or fresh_body != disk_body:
        return [_finding(
            "index_freshness", "INDEX.md",
            "INDEX.md content does not match a freshly regenerated index — it is stale "
            "relative to context/latest.md, decisions/*.md, preferences.md, and todos.json.",
            "Call any memory write workflow, or regenerate_index()+write_index(), to refresh it.",
        )]
    return []


