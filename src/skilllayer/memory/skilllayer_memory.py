def _check_decision_status_consistency(records: list[dict[str, Any]]) -> list[dict[str, Any]]:
    findings = []
    for r in records:
        status = r["meta"].get("status")
        superseded_by = r["meta"].get("superseded_by")
        if status == "superseded" and not superseded_by:
            findings.append(_finding(
                "decision_status_superseded_by_mismatch", f"decisions/{r['filename']}",
                "status is 'superseded' but superseded_by is not set.",
                "Set superseded_by to the id that supersedes this decision, or correct the status.",
            ))
        elif status != "superseded" and superseded_by:
            findings.append(_finding(
                "decision_status_superseded_by_mismatch", f"decisions/{r['filename']}",
                f"superseded_by={superseded_by!r} is set but status is {status!r}, not 'superseded'.",
                "Set status to 'superseded', or clear the superseded_by field.",
            ))
    return findings


def _check_decision_cycles(by_id: dict[str, list[dict[str, Any]]]) -> list[dict[str, Any]]:
    edges: dict[str, str | None] = {}
    for dec_id, recs in by_id.items():
        target = recs[0]["meta"].get("supersedes")
        edges[dec_id] = str(target) if target else None
    cycles = _find_supersession_cycles(edges)
    return [
        _finding(
            "decision_supersession_cycle", "decisions/",
            f"supersession cycle detected: {' -> '.join(cycle)}.",
            "Manually break the cycle by correcting one decision's supersedes field.",
        )
        for cycle in cycles
    ]


