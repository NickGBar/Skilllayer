# RehydrateContext (read-only)
# ---------------------------------------------------------------------------

def rehydrate_context(
    skilllayer_dir: Path,
    *,
    full_context: bool = False,
    decision_id: str | None = None,
    full_preferences: bool = False,
) -> dict[str, Any]:
    """Read-only. Returns INDEX.md digest plus optional drill-down content.

    A repository with no .skilllayer/ store yet is a valid, healthy EMPTY
    state — nothing has been saved, not a failure — so this reports
    status="warning"/success=true (a usable, if empty, result), never
    status="error"/success=false. error_code stays None: there is nothing
    wrong, just nothing saved yet."""
    index_file = skilllayer_dir / "INDEX.md"
    if not index_file.exists():
        return finalize_memory_result(
            {
                "workflow": "RehydrateContextWorkflow",
                "index": None,
                "context": None,
                "decision": None,
                "preferences": None,
                "error_code": None,
                "summary": "No .skilllayer/ memory store found at this repository root.",
            },
            status="warning",
            warnings=[
                "No .skilllayer/ memory store found yet at this repository root — nothing "
                "has been saved. Save a context snapshot first using the MCP tool "
                "'skilllayer_save_context' (the CLI cannot perform memory write operations)."
            ],
        )

    index_content = index_file.read_text(encoding="utf-8")
    context_content: str | None = None
    decision_content: str | None = None
    preferences_content: str | None = None

    if full_context:
        latest = skilllayer_dir / "context" / "latest.md"
        if latest.exists():
            context_content = latest.read_text(encoding="utf-8")

    if decision_id is not None:
        decisions_dir = skilllayer_dir / "decisions"
        matches = list(decisions_dir.glob(f"{decision_id}-*.md")) if decisions_dir.exists() else []
        if matches:
            decision_content = matches[0].read_text(encoding="utf-8")
        else:
            decision_content = f"(decision {decision_id} not found)"

    if full_preferences:
        prefs_file = skilllayer_dir / "preferences.md"
        if prefs_file.exists():
            preferences_content = prefs_file.read_text(encoding="utf-8")

    has_extras = any(x is not None for x in (context_content, decision_content, preferences_content))
    summary = "Memory rehydrated from INDEX.md."
    if has_extras:
        parts = []
        if context_content:
            parts.append("full context")
        if decision_content:
            parts.append(f"decision {decision_id}")
        if preferences_content:
            parts.append("full preferences")
        summary = f"Memory rehydrated: index + {', '.join(parts)}."

    # Recompute NeedsWrap LIVE rather than trusting the value baked into
    # INDEX.md at the last write — file changes may have happened since the
    # index was last regenerated, and the whole point is to catch exactly that.
    needs_wrap = compute_needs_wrap(skilllayer_dir)

    return finalize_memory_result({
        "workflow": "RehydrateContextWorkflow",
        "index": index_content,
        "context": context_content,
        "decision": decision_content,
        "preferences": preferences_content,
        "needs_wrap": {
            "status": needs_wrap["status"],
            "changed_count": needs_wrap["changed_count"],
            "last_snapshot": needs_wrap["last_snapshot"],
            "last_file_watch": needs_wrap["last_file_watch"],
            "changed_sample": needs_wrap["changed_sample"],
        },
        "error_code": None,
        "summary": summary,
    })


# ---------------------------------------------------------------------------
# CrossSessionTodo
#
# Distinct from SaveContextSnapshot's Open Questions: a todo has a stable id,
# an explicit status, and is added/completed independently of any narrative
# snapshot rewrite. Flat JSON (not markdown+frontmatter) because a todo is
# short structured bookkeeping, not prose — same split as
# file_watch_snapshot.json vs. the decisions/*.md files.
# ---------------------------------------------------------------------------

TODO_TEXT_MAX_LENGTH = 500
TODOS_FILENAME = "todos.json"
TODO_INDEX_DISPLAY_CAP = 20


class TodosStoreCorruptError(Exception):
    """Raised by _read_todos when todos.json EXISTS but cannot be parsed as the
    expected {"todos": [...]} shape.

    Deliberately distinct from "file does not exist yet" (a normal, safe
    default for a brand-new store). Previously, a corrupt-but-present file
    silently fell back to an empty in-memory todos list, and the very next
    add_todo/mark_todo_done write would persist that empty list back to disk —
    permanently wiping every real todo. Callers must catch this and refuse to
    write rather than silently trusting the fallback as new source of truth.
    """


def _read_todos(skilllayer_dir: Path) -> dict[str, Any]:
    todos_file = skilllayer_dir / TODOS_FILENAME
    if not todos_file.exists():
        return {"schema": SCHEMA_VERSION, "next_id": 1, "todos": []}
    try:
        data = json.loads(todos_file.read_text(encoding="utf-8"))
    except (json.JSONDecodeError, OSError) as exc:
        raise TodosStoreCorruptError(f"{TODOS_FILENAME} exists but is not valid JSON: {exc}") from exc
    if not (isinstance(data, dict) and isinstance(data.get("todos"), list)):
        raise TodosStoreCorruptError(f"{TODOS_FILENAME} exists but is not shaped as {{'todos': [...]}}.")
    return data


def _write_todos(skilllayer_dir: Path, data: dict[str, Any]) -> None:
    atomic_write_json(skilllayer_dir / TODOS_FILENAME, data)


def add_todo(skilllayer_dir: Path, text: str) -> dict[str, Any]:
    """Add one todo. Zero LLM calls.

    The read + claim-id + mutate + write + reindex sequence runs as one
    transaction under memory_lock, so two concurrent AddTodo calls can never
    claim the same id or lose one call's todo to the other's write.

    NOTE: this result's top-level "status" field is the ADDED TODO's own
    state ("open"), not the shared health-status word — see
    finalize_memory_result's include_status_field docstring. success/
    error_code/warnings still follow the shared contract."""
    stripped = text.strip()
    if not stripped:
        return finalize_memory_result({
            "workflow": "AddTodoWorkflow",
            "todo_id": None,
            "text": None,
            "status": None,
            "created": None,
            "error_code": "empty_todo_text",
            "error": "Todo text must not be empty.",
            "summary": "No todo added: text was empty.",
        }, include_status_field=False)
    if len(stripped) > TODO_TEXT_MAX_LENGTH:
        return finalize_memory_result({
            "workflow": "AddTodoWorkflow",
            "todo_id": None,
            "text": None,
            "status": None,
            "created": None,
            "error_code": "todo_text_too_long",
            "error": f"Todo text exceeds {TODO_TEXT_MAX_LENGTH} characters ({len(stripped)}). Keep todos short and actionable.",
            "summary": f"No todo added: text exceeds {TODO_TEXT_MAX_LENGTH} characters.",
        }, include_status_field=False)

    skilllayer_dir.mkdir(parents=True, exist_ok=True)

    try:
        with memory_lock(skilllayer_dir):
            # Read (and validate) BEFORE claiming an id, so a corrupt todos.json
            # fails fast without burning a counter slot on a write that never
            # happens.
            try:
                data = _read_todos(skilllayer_dir)
            except TodosStoreCorruptError as exc:
                return finalize_memory_result({
                    "workflow": "AddTodoWorkflow",
                    "todo_id": None,
                    "text": None,
                    "status": None,
                    "created": None,
                    "error_code": ERROR_TODOS_STORE_CORRUPT,
                    "error": str(exc),
                    "summary": "No todo added: todos.json exists but is corrupt. Fix or remove it before adding todos.",
                }, include_status_field=False)

            try:
                todo_id = claim_next_id(skilllayer_dir, "next_todo_id")
            except MemoryStateCorruptError as exc:
                return finalize_memory_result({
                    "workflow": "AddTodoWorkflow",
                    "todo_id": None,
                    "text": None,
                    "status": None,
                    "created": None,
                    "error_code": ERROR_MEMORY_STATE_CORRUPT,
                    "error": str(exc),
                    "summary": "No todo added: .state.json exists but is corrupt. Fix or remove it before adding todos.",
                }, include_status_field=False)
            now = _utc_iso()
            todo = {"id": todo_id, "text": stripped, "status": "open", "created": now, "done_at": None}

            data["todos"].append(todo)
            _write_todos(skilllayer_dir, data)
            write_index(skilllayer_dir)
    except MemoryLockTimeoutError as exc:
        return finalize_memory_result({
            "workflow": "AddTodoWorkflow",
            "todo_id": None,
            "text": None,
            "status": None,
            "created": None,
            "error_code": ERROR_MEMORY_LOCK_TIMEOUT,
            "error": str(exc),
            "summary": "No todo added: could not acquire the memory lock in time.",
        }, include_status_field=False)
    except PermissionError as exc:
        return finalize_memory_result({
            "workflow": "AddTodoWorkflow",
            "todo_id": None,
            "text": None,
            "status": None,
            "created": None,
            "error_code": ERROR_MEMORY_PERMISSION_DENIED,
            "error": str(exc),
            "summary": "No todo added: permission denied writing to .skilllayer/.",
        }, include_status_field=False)
    except OSError as exc:
        return finalize_memory_result({
            "workflow": "AddTodoWorkflow",
            "todo_id": None,
            "text": None,
            "status": None,
            "created": None,
            "error_code": ERROR_MEMORY_IO_ERROR,
            "error": str(exc),
            "summary": "No todo added: an I/O error occurred writing to .skilllayer/.",
        }, include_status_field=False)

    return finalize_memory_result({
        "workflow": "AddTodoWorkflow",
        "todo_id": todo_id,
        "text": stripped,
        "status": "open",
        "created": now,
        "error_code": None,
        "error": None,
        "summary": f"Todo {todo_id} added: {stripped!r}.",
    }, include_status_field=False)


def mark_todo_done(skilllayer_dir: Path, todo_id: str, status: str = "done") -> dict[str, Any]:
    """Set a todo's status. status="done" completes it (stamps done_at);
    status="open" reopens a previously-done todo (clears done_at). Zero LLM
    calls.

    The read + mutate + write + reindex sequence runs as one transaction
    under memory_lock, so two concurrent status updates (including to
    different todos in the same store) cannot interleave and lose one
    update to the other's write.

    NOTE: this result's top-level "status" field is the TOUCHED TODO's own
    state ("open"/"done"), not the shared health-status word — see
    finalize_memory_result's include_status_field docstring. success/
    error_code/warnings still follow the shared contract."""
    if status not in ("open", "done"):
        return finalize_memory_result({
            "workflow": "MarkTodoDoneWorkflow",
            "todo_id": todo_id,
            "status": None,
            "previous_status": None,
            "done_at": None,
            "error_code": "invalid_status",
            "error": f"status must be 'open' or 'done', got {status!r}.",
            "summary": "No change: invalid target status.",
        }, include_status_field=False)

    try:
        with memory_lock(skilllayer_dir):
            try:
                data = _read_todos(skilllayer_dir)
            except TodosStoreCorruptError as exc:
                return finalize_memory_result({
                    "workflow": "MarkTodoDoneWorkflow",
                    "todo_id": todo_id,
                    "status": None,
                    "previous_status": None,
                    "done_at": None,
                    "error_code": ERROR_TODOS_STORE_CORRUPT,
                    "error": str(exc),
                    "summary": "No change: todos.json exists but is corrupt. Fix or remove it before updating todos.",
                }, include_status_field=False)
            target = next((t for t in data["todos"] if t["id"] == todo_id), None)
            if target is None:
                return finalize_memory_result({
                    "workflow": "MarkTodoDoneWorkflow",
                    "todo_id": todo_id,
                    "status": None,
                    "previous_status": None,
                    "done_at": None,
                    "error_code": ERROR_MEMORY_RECORD_NOT_FOUND,
                    "error": f"No todo with id {todo_id!r}.",
                    "summary": f"No change: todo {todo_id!r} not found.",
                }, include_status_field=False)

            previous_status = target["status"]
            target["status"] = status
            target["done_at"] = _utc_iso() if status == "done" else None

            _write_todos(skilllayer_dir, data)
            write_index(skilllayer_dir)
    except MemoryLockTimeoutError as exc:
        return finalize_memory_result({
            "workflow": "MarkTodoDoneWorkflow",
            "todo_id": todo_id,
            "status": None,
            "previous_status": None,
            "done_at": None,
            "error_code": ERROR_MEMORY_LOCK_TIMEOUT,
            "error": str(exc),
            "summary": "No change: could not acquire the memory lock in time.",
        }, include_status_field=False)
    except PermissionError as exc:
        return finalize_memory_result({
            "workflow": "MarkTodoDoneWorkflow",
            "todo_id": todo_id,
            "status": None,
            "previous_status": None,
            "done_at": None,
            "error_code": ERROR_MEMORY_PERMISSION_DENIED,
            "error": str(exc),
            "summary": "No change: permission denied writing to .skilllayer/.",
        }, include_status_field=False)
    except OSError as exc:
        return finalize_memory_result({
            "workflow": "MarkTodoDoneWorkflow",
            "todo_id": todo_id,
            "status": None,
            "previous_status": None,
            "done_at": None,
            "error_code": ERROR_MEMORY_IO_ERROR,
            "error": str(exc),
            "summary": "No change: an I/O error occurred writing to .skilllayer/.",
        }, include_status_field=False)

    return finalize_memory_result({
        "workflow": "MarkTodoDoneWorkflow",
        "todo_id": todo_id,
        "status": status,
        "previous_status": previous_status,
        "done_at": target["done_at"],
        "error_code": None,
        "error": None,
        "summary": f"Todo {todo_id} marked {status}.",
    }, include_status_field=False)


def list_todos(skilllayer_dir: Path, status_filter: str = "open") -> dict[str, Any]:
    """List/filter todos. Read-only. Zero LLM calls.

    Zero matching todos for a valid filter is a HEALTHY empty result
    (error_code stays None), never treated as broken merely because the list
    is empty — same principle SearchDecisionLogWorkflow already applies to
    zero search matches."""
    if status_filter not in ("open", "done", "all"):
        return finalize_memory_result({
            "workflow": "ListTodosWorkflow",
            "filter_status": status_filter,
            "todos": [],
            "open_count": 0,
            "done_count": 0,
            "total_count": 0,
            "error_code": "invalid_status_filter",
            "error": f"status_filter must be 'open', 'done', or 'all', got {status_filter!r}.",
            "summary": "No results: invalid status filter.",
        })

    try:
        data = _read_todos(skilllayer_dir)
    except TodosStoreCorruptError as exc:
        return finalize_memory_result({
            "workflow": "ListTodosWorkflow",
            "filter_status": status_filter,
            "todos": [],
            "open_count": 0,
            "done_count": 0,
            "total_count": 0,
            "error_code": ERROR_TODOS_STORE_CORRUPT,
            "error": str(exc),
            "summary": "No results: todos.json exists but is corrupt.",
        })
    all_todos = data["todos"]
    open_count = sum(1 for t in all_todos if t["status"] == "open")
    done_count = sum(1 for t in all_todos if t["status"] == "done")
    filtered = all_todos if status_filter == "all" else [t for t in all_todos if t["status"] == status_filter]

    return finalize_memory_result({
        "workflow": "ListTodosWorkflow",
        "filter_status": status_filter,
        "todos": filtered,
        "open_count": open_count,
        "done_count": done_count,
        "total_count": len(all_todos),
        "error_code": None,
        "error": None,
        "summary": f"{open_count} open todo(s), {done_count} done.",
    })


# ---------------------------------------------------------------------------
# ValidateMemoryWorkflow — standalone, on-demand, read-only integrity check
#
# Unlike NeedsWrap, this is NOT wired into regenerate_index()/rehydrate_context:
# it reads and fully parses every decisions/*.md file, todos.json, and
# preferences.md and walks the supersession graph — an O(N) cost over the
# whole store, not appropriate to pay on every single memory write. It is a
# deliberate, standalone diagnostic: call it when you want to know whether the
# store is internally consistent, not automatically on every write.
#
# Read-only: never writes .state.json, INDEX.md, or any memory file. Never
# raises — a missing/corrupt file is reported as a finding, not an exception.
# ---------------------------------------------------------------------------

CHECK_SEVERITY: dict[str, str] = {
    "index_freshness": "warning",
    "context_frontmatter_schema": "broken",
    "context_missing_sections": "broken",
    "decision_frontmatter_schema": "broken",
    "decision_status_invalid": "broken",
    "decision_filename_id_mismatch": "broken",
    "decision_duplicate_id": "broken",
    "decision_supersedes_dangling": "broken",
    "decision_superseded_by_dangling": "broken",
    "decision_status_superseded_by_mismatch": "broken",
    "decision_supersession_cycle": "broken",
    "preferences_frontmatter_schema": "broken",
    "preferences_unparseable_line": "warning",
    "todos_file_shape": "broken",
    "todos_entry_missing_fields": "broken",
    "todos_status_invalid": "broken",
    "todos_done_at_mismatch": "warning",
    "todos_duplicate_id": "broken",
    "decision_counter_behind": "broken",
    "todo_counter_behind": "broken",
}

_DECISION_REQUIRED_FIELDS = ("schema", "id", "status", "created")
_CONTEXT_REQUIRED_FIELDS = ("schema", "type", "id", "created")
_PREFERENCES_REQUIRED_FIELDS = ("schema", "type", "id")
_TODO_REQUIRED_FIELDS = ("id", "text", "status", "created", "done_at")
_DECISION_STATUSES = ("proposed", "accepted", "superseded", "deprecated")
_DECISION_FILENAME_RE = re.compile(r"^(\d{4})-")


def _finding(check: str, file: str | None, message: str, suggested_fix: str) -> dict[str, Any]:
    return {
        "check": check,
        "severity": CHECK_SEVERITY[check],
        "file": file,
        "message": message,
        "suggested_fix": suggested_fix,
    }


def _try_read_json(path: Path) -> Any:
    """Lenient JSON read. Returns None on any missing/unreadable/invalid input.
    Never raises — used only by the validator, which must never crash."""
    if not path.exists():
        return None
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except (json.JSONDecodeError, OSError):
        return None


def _has_heading(body: str, heading: str) -> bool:
    pat = re.compile(r"^#{1,6}\s+" + re.escape(heading) + r"\s*$", re.MULTILINE)
    return bool(pat.search(body))


def _max_numeric_id(ids: Any) -> int:
    best = 0
    for i in ids:
        try:
            best = max(best, int(str(i)))
        except (TypeError, ValueError):
            continue
    return best


def _load_decision_records(skilllayer_dir: Path) -> list[dict[str, Any]]:
    """Read every decisions/*.md file leniently. Never raises."""
    decisions_dir = skilllayer_dir / "decisions"
    records: list[dict[str, Any]] = []
    if not decisions_dir.exists():
        return records
    for path in sorted(decisions_dir.glob("*.md")):
        try:
            raw = path.read_text(encoding="utf-8")
        except OSError:
            continue
        meta, body = parse_frontmatter(raw)
        records.append({"filename": path.name, "meta": meta, "body": body})
    return records


def _decision_effective_id(record: dict[str, Any]) -> str | None:
    """The id used for chain lookups: prefer frontmatter id, else filename prefix."""
    meta_id = record["meta"].get("id")
    if meta_id is not None:
        return str(meta_id)
    m = _DECISION_FILENAME_RE.match(record["filename"])
    return m.group(1) if m else None


def _index_decisions_by_id(
    records: list[dict[str, Any]],
) -> tuple[dict[str, list[dict[str, Any]]], dict[str, list[str]]]:
    by_id: dict[str, list[dict[str, Any]]] = {}
    for r in records:
        eff_id = _decision_effective_id(r)
        if eff_id is None:
            continue
        by_id.setdefault(eff_id, []).append(r)
    duplicates = {k: [r["filename"] for r in v] for k, v in by_id.items() if len(v) > 1}
    return by_id, duplicates


def _find_supersession_cycles(edges: dict[str, str | None]) -> list[list[str]]:
    """Detect cycles (including a direct self-reference) in the supersedes
    graph. Dangling targets (not a key in `edges`) are dead ends, not cycle
    participants — those are reported separately as dangling references."""
    WHITE, GRAY, BLACK = 0, 1, 2
    color = {k: WHITE for k in edges}
    cycles: list[list[str]] = []
    reported: set[frozenset] = set()

    def visit(node: str, path: list[str]) -> None:
        color[node] = GRAY
        path.append(node)
        target = edges.get(node)
        if target is not None and target in edges:
            if color[target] == GRAY:
                idx = path.index(target)
                cycle = path[idx:] + [target]
                key = frozenset(cycle[:-1])
                if key not in reported:
                    cycles.append(cycle)
                    reported.add(key)
            elif color[target] == WHITE:
                visit(target, path)
        path.pop()
        color[node] = BLACK

    for node in list(edges):
        if color[node] == WHITE:
            visit(node, [])
    return cycles


