def _check_preferences_lines(prefs_exists: bool, prefs_body: str) -> list[dict[str, Any]]:
    if not prefs_exists:
        return []
    findings = []
    current_domain: str | None = None
    for i, line in enumerate(prefs_body.splitlines(), start=1):
        stripped = line.strip()
        if not stripped:
            continue
        if stripped.startswith("## "):
            current_domain = stripped[3:].strip()
            continue
        if stripped.startswith("- ") and current_domain and ": " in stripped[2:]:
            continue
        findings.append(_finding(
            "preferences_unparseable_line", "preferences.md",
            f"line {i} does not match the expected '## domain' or '- key: value' shape "
            f"and will be silently dropped on the next write: {stripped!r}",
            "Rewrite the line as '- key: value' under a '## domain' heading, or remove it.",
        ))
    return findings


# --- Group C: todos.json ------------------------------------------------------

def _check_todos_shape(todos_exists: bool, todos_shape_ok: bool) -> list[dict[str, Any]]:
    if todos_exists and not todos_shape_ok:
        return [_finding(
            "todos_file_shape", TODOS_FILENAME,
            f"{TODOS_FILENAME} exists but is not valid JSON, or is not shaped as {{'todos': [...]}}.",
            "Fix or remove the file to resume normal todo writes. The next add_todo/"
            "mark_todo_done call will correctly refuse to write over it rather than "
            "silently discarding its contents.",
        )]
    return []


def _check_todos_entry_fields(todos_shape_ok: bool, todos_list: list[Any]) -> list[dict[str, Any]]:
    if not todos_shape_ok:
        return []
    findings = []
    for i, t in enumerate(todos_list):
        if not isinstance(t, dict):
            findings.append(_finding("todos_entry_missing_fields", TODOS_FILENAME,
                                      f"todos[{i}] is not an object.",
                                      "Remove or repair the malformed entry."))
            continue
        missing = [k for k in _TODO_REQUIRED_FIELDS if k not in t]
        if missing:
            tid = t.get("id", f"index {i}")
            findings.append(_finding(
                "todos_entry_missing_fields", TODOS_FILENAME,
                f"todo {tid!r} is missing required field(s): {', '.join(missing)}.",
                "Add the missing field(s), matching the shape written by AddTodoWorkflow.",
            ))
    return findings


def _check_todos_status(todos_shape_ok: bool, todos_list: list[Any]) -> list[dict[str, Any]]:
    if not todos_shape_ok:
        return []
    findings = []
    for t in todos_list:
        if not isinstance(t, dict):
            continue
        status = t.get("status")
        if "status" in t and status not in ("open", "done"):
            findings.append(_finding(
                "todos_status_invalid", TODOS_FILENAME,
                f"todo {t.get('id')!r} has status {status!r}, not 'open' or 'done'.",
                "Correct the status field to 'open' or 'done'.",
            ))
    return findings


def _check_todos_done_at(todos_shape_ok: bool, todos_list: list[Any]) -> list[dict[str, Any]]:
    if not todos_shape_ok:
        return []
    findings = []
    for t in todos_list:
        if not isinstance(t, dict) or "status" not in t or "done_at" not in t:
            continue
        status, done_at = t.get("status"), t.get("done_at")
        if status == "done" and not done_at:
            findings.append(_finding("todos_done_at_mismatch", TODOS_FILENAME,
                                      f"todo {t.get('id')!r} has status 'done' but done_at is not set.",
                                      "Set done_at, or reopen the todo via MarkTodoDone(status='open')."))
        elif status == "open" and done_at:
            findings.append(_finding("todos_done_at_mismatch", TODOS_FILENAME,
                                      f"todo {t.get('id')!r} has status 'open' but done_at is set to {done_at!r}.",
                                      "Clear done_at, or mark the todo done via MarkTodoDone(status='done')."))
    return findings


def _check_todos_duplicates(todos_shape_ok: bool, todos_list: list[Any]) -> list[dict[str, Any]]:
    if not todos_shape_ok:
        return []
    ids: dict[str, int] = {}
    for t in todos_list:
        if isinstance(t, dict) and "id" in t:
            ids[str(t["id"])] = ids.get(str(t["id"]), 0) + 1
    return [
        _finding("todos_duplicate_id", TODOS_FILENAME,
                 f"todo id {tid!r} is used by {count} entries.",
                 "Renumber the colliding todos to unique ids (this can happen if .state.json's "
                 "next_todo_id counter was reset or edited).")
        for tid, count in sorted(ids.items()) if count > 1
    ]


# --- Group D: ID counter drift ------------------------------------------------

def _check_decision_counter(state_data: dict[str, Any], by_id: dict[str, list[dict[str, Any]]]) -> list[dict[str, Any]]:
    if not by_id:
        return []
    raw = state_data.get("next_decision_id", 1)
    try:
        next_id = int(raw)
    except (TypeError, ValueError):
        return [_finding("decision_counter_behind", ".state.json",
                         f"next_decision_id is not a valid integer: {raw!r}.",
                         "Repair .state.json's next_decision_id to an integer greater than the highest existing decision id.")]
    max_existing = _max_numeric_id(by_id.keys())
    if next_id <= max_existing:
        return [_finding(
            "decision_counter_behind", ".state.json",
            f"next_decision_id={next_id} is <= the highest existing decision id ({max_existing:04d}); "
            "the next TrackDecisionLog call will mint a duplicate id.",
            "Set .state.json's next_decision_id to at least one greater than the highest existing decision id.",
        )]
    return []


def _check_todo_counter(state_data: dict[str, Any], todos_shape_ok: bool, todos_list: list[Any]) -> list[dict[str, Any]]:
    if not todos_shape_ok:
        return []
    ids = [t["id"] for t in todos_list if isinstance(t, dict) and "id" in t]
    if not ids:
        return []
    raw = state_data.get("next_todo_id", 1)
    try:
        next_id = int(raw)
    except (TypeError, ValueError):
        return [_finding("todo_counter_behind", ".state.json",
                         f"next_todo_id is not a valid integer: {raw!r}.",
                         "Repair .state.json's next_todo_id to an integer greater than the highest existing todo id.")]
    max_existing = _max_numeric_id(ids)
    if next_id <= max_existing:
        return [_finding(
            "todo_counter_behind", ".state.json",
            f"next_todo_id={next_id} is <= the highest existing todo id ({max_existing:04d}); "
            "the next AddTodo call will mint a duplicate id.",
            "Set .state.json's next_todo_id to at least one greater than the highest existing todo id.",
        )]
    return []


def validate_memory_store(skilllayer_dir: Path) -> dict[str, Any]:
    """Check the .skilllayer/ memory store for internal consistency.

    Standalone, on-demand, read-only. Never writes anything and never raises —
    a missing or corrupt file is reported as a finding, not an exception.
    Returns store_exists: False (zero checks run) if skilllayer_dir itself
    does not exist yet.
    """
    checked_at = _utc_iso()
    if not skilllayer_dir.exists():
        # No store yet is a valid, healthy-adjacent EMPTY state (nothing has
        # been saved), never "broken" — same treatment as
        # RehydrateContextWorkflow's identical condition.
        return finalize_memory_result(
            {
                "workflow": "ValidateMemoryWorkflow",
                "store_exists": False,
                "checked_at": checked_at,
                "summary": {"checks_run": 0, "checks_passed": 0, "warnings_count": 0, "broken_count": 0},
                "findings": [],
                "error_code": None,
            },
            status="warning",
            warnings=["No .skilllayer/ memory store found yet at this repository root — nothing to validate."],
        )

    # --- LoadMemoryStore ---
    index_path = skilllayer_dir / "INDEX.md"
    index_text = index_path.read_text(encoding="utf-8") if index_path.exists() else None

    latest_path = skilllayer_dir / "context" / "latest.md"
    latest_exists = latest_path.exists()
    ctx_meta, ctx_body = ({}, "")
    if latest_exists:
        ctx_meta, ctx_body = parse_frontmatter(latest_path.read_text(encoding="utf-8"))

    decision_records = _load_decision_records(skilllayer_dir)
    by_id, duplicate_decision_ids = _index_decisions_by_id(decision_records)

    prefs_path = skilllayer_dir / "preferences.md"
    prefs_exists = prefs_path.exists()
    prefs_meta, prefs_body = ({}, "")
    if prefs_exists:
        prefs_meta, prefs_body = parse_frontmatter(prefs_path.read_text(encoding="utf-8"))

    todos_path = skilllayer_dir / TODOS_FILENAME
    todos_exists = todos_path.exists()
    todos_raw = _try_read_json(todos_path)
    todos_shape_ok = isinstance(todos_raw, dict) and isinstance(todos_raw.get("todos"), list)
    todos_list: list[Any] = todos_raw["todos"] if todos_shape_ok else []

    state_raw = _try_read_json(skilllayer_dir / ".state.json")
    state_data: dict[str, Any] = state_raw if isinstance(state_raw, dict) else {}

    # --- CheckIndexFreshness / CheckFrontmatterSchemas / CheckDecisionChainIntegrity / CheckIdCounters ---
    findings_by_check: dict[str, list[dict[str, Any]]] = {
        "index_freshness": _check_index_freshness(skilllayer_dir, index_text),
        "context_frontmatter_schema": _check_context_frontmatter(latest_exists, ctx_meta),
        "context_missing_sections": _check_context_sections(latest_exists, ctx_body),
        "decision_frontmatter_schema": _check_decision_frontmatter(decision_records),
        "decision_status_invalid": _check_decision_status(decision_records),
        "decision_filename_id_mismatch": _check_decision_filename(decision_records),
        "decision_duplicate_id": _check_decision_duplicates(duplicate_decision_ids),
        "decision_supersedes_dangling": _check_decision_supersedes_dangling(decision_records, by_id),
        "decision_superseded_by_dangling": _check_decision_superseded_by_dangling(decision_records, by_id),
        "decision_status_superseded_by_mismatch": _check_decision_status_consistency(decision_records),
        "decision_supersession_cycle": _check_decision_cycles(by_id),
        "preferences_frontmatter_schema": _check_preferences_frontmatter(prefs_exists, prefs_meta),
        "preferences_unparseable_line": _check_preferences_lines(prefs_exists, prefs_body),
        "todos_file_shape": _check_todos_shape(todos_exists, todos_shape_ok),
        "todos_entry_missing_fields": _check_todos_entry_fields(todos_shape_ok, todos_list),
        "todos_status_invalid": _check_todos_status(todos_shape_ok, todos_list),
        "todos_done_at_mismatch": _check_todos_done_at(todos_shape_ok, todos_list),
        "todos_duplicate_id": _check_todos_duplicates(todos_shape_ok, todos_list),
        "decision_counter_behind": _check_decision_counter(state_data, by_id),
        "todo_counter_behind": _check_todo_counter(state_data, todos_shape_ok, todos_list),
    }

    # --- BuildIntegrityReport ---
    findings: list[dict[str, Any]] = []
    checks_passed = warnings_count = broken_count = 0
    for name, severity in CHECK_SEVERITY.items():
        items = findings_by_check[name]
        if not items:
            checks_passed += 1
        elif severity == "broken":
            broken_count += 1
        else:
            warnings_count += 1
        findings.extend(items)

    # status is the single authoritative verdict for the whole store:
    #   any broken finding -> "broken" (data integrity invalid, success=false)
    #   only warning findings -> "warning" (usable, but flagged, success=true)
    #   zero findings -> "healthy" (success=true)
    if broken_count > 0:
        result_status = "broken"
        error_code: str | None = ERROR_MEMORY_INTEGRITY_BROKEN
    else:
        result_status = "warning" if warnings_count > 0 else "healthy"
        error_code = None

    warning_messages = [f["message"] for f in findings if f["severity"] == "warning"]

    return finalize_memory_result(
        {
            "workflow": "ValidateMemoryWorkflow",
            "store_exists": True,
            "checked_at": checked_at,
            "summary": {
                "checks_run": len(CHECK_SEVERITY),
                "checks_passed": checks_passed,
                "warnings_count": warnings_count,
                "broken_count": broken_count,
            },
            "findings": findings,
            "error_code": error_code,
        },
        status=result_status,
        warnings=warning_messages,
    )
