"""SkillLayer .skilllayer/ persistence infrastructure.

Storage root: <repo>/.skilllayer/
Source of truth: markdown + YAML-like frontmatter (schema: 1)
No PyYAML dependency — minimal key:value parser.
Zero LLM calls in all read and write paths.

Directory layout:
  .skilllayer/
    INDEX.md            — digest, regenerated by every write workflow
    context/
      latest.md         — SaveContextSnapshot (latest-wins, gitignored)
      history/          — rotated snapshots, keep last HISTORY_KEEP
    decisions/
      0001-slug.md      — TrackDecisionLog (append-only, immutable)
    preferences.md      — RememberUserPreferences (upsert per section)
    .state.json         — sequence bookkeeping only, never read by Claude
"""
