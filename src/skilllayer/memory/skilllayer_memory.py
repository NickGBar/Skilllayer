def track_decision(
    skilllayer_dir: Path,
    title: str,
    context: str,
    decision: str,
    reasoning: str,
    consequences: str,
    status: str = "proposed",
    related_files: list[str] | None = None,
    supersedes: str | None = None,
) -> dict[str, Any]:
    """Append an immutable ADR file to decisions/.

    The full sequence — claiming an id, writing the new file, flipping any
    superseded file's status, and regenerating the index — runs as one
    transaction under memory_lock, so two concurrent TrackDecisionLog calls
    can never claim the same id or interleave a supersession update."""
    if status not in ("proposed", "accepted", "superseded", "deprecated"):
        status = "proposed"

    skilllayer_dir.mkdir(parents=True, exist_ok=True)
    decisions_dir = skilllayer_dir / "decisions"
    decisions_dir.mkdir(exist_ok=True)

    try:
        with memory_lock(skilllayer_dir):
            try:
                decision_id = claim_next_id(skilllayer_dir, "next_decision_id")
            except MemoryStateCorruptError as exc:
                # include_status_field=False: "status" here is the decision's
                # own proposed/accepted/superseded/deprecated state, not the
                # health-status word (same collision as Add/MarkTodoDone).
                return finalize_memory_result(
                    {
                        "workflow": "TrackDecisionLogWorkflow",
                        "decision_id": None,
                        "file": None,
                        "title": title,
                        "status": None,
                        "supersedes": supersedes,
                        "error_code": ERROR_MEMORY_STATE_CORRUPT,
                        "error": str(exc),
                        "summary": "No decision recorded: .state.json exists but is corrupt. Fix or remove it before recording decisions.",
                    },
                    include_status_field=False,
                )

            slug = _slugify(title)
            filename = f"{decision_id}-{slug}.md"
            decision_file = decisions_dir / filename

            now = _utc_iso()
            meta: dict[str, Any] = {
                "schema": SCHEMA_VERSION,
                "type": "decision",
                "workflow": "TrackDecisionLogWorkflow",
                "id": decision_id,
                "status": status,
                "supersedes": supersedes,
                "created": now,
                "updated": now,
            }
            if related_files:
                meta["related_files"] = related_files

            body = (
                f"# {title}\n\n"
                f"## Context\n{context}\n\n"
                f"## Decision\n{decision}\n\n"
                f"## Reasoning\n{reasoning}\n\n"
                f"## Consequences\n{consequences}\n"
            )
            atomic_write_text(decision_file, write_frontmatter(meta, body))

            # If this supersedes an older decision, flip its status
            if supersedes:
                old_files = list(decisions_dir.glob(f"{supersedes}-*.md"))
                for old_file in old_files:
                    old_meta, old_body = parse_frontmatter(old_file.read_text(encoding="utf-8"))
                    old_meta["status"] = "superseded"
                    old_meta["superseded_by"] = decision_id
                    atomic_write_text(old_file, write_frontmatter(old_meta, old_body))

            write_index(skilllayer_dir)
    except MemoryLockTimeoutError as exc:
        return finalize_memory_result(
            {
                "workflow": "TrackDecisionLogWorkflow",
                "decision_id": None,
                "file": None,
                "title": title,
                "status": None,
                "supersedes": supersedes,
                "error_code": ERROR_MEMORY_LOCK_TIMEOUT,
                "error": str(exc),
                "summary": "No decision recorded: could not acquire the memory lock in time.",
            },
            include_status_field=False,
        )
    except PermissionError as exc:
        return finalize_memory_result(
            {
                "workflow": "TrackDecisionLogWorkflow",
                "decision_id": None,
                "file": None,
                "title": title,
                "status": None,
                "supersedes": supersedes,
                "error_code": ERROR_MEMORY_PERMISSION_DENIED,
                "error": str(exc),
                "summary": "No decision recorded: permission denied writing to .skilllayer/.",
            },
            include_status_field=False,
        )
    except OSError as exc:
        return finalize_memory_result(
            {
                "workflow": "TrackDecisionLogWorkflow",
                "decision_id": None,
                "file": None,
                "title": title,
                "status": None,
                "supersedes": supersedes,
                "error_code": ERROR_MEMORY_IO_ERROR,
                "error": str(exc),
                "summary": "No decision recorded: an I/O error occurred writing to .skilllayer/.",
            },
            include_status_field=False,
        )

    return finalize_memory_result(
        {
            "workflow": "TrackDecisionLogWorkflow",
            "decision_id": decision_id,
            "file": str(decision_file),
            "title": title,
            "status": status,
            "supersedes": supersedes,
            "error_code": None,
            "error": None,
            "summary": f"Decision {decision_id} recorded: {title!r} ({status}).",
        },
        include_status_field=False,
    )


