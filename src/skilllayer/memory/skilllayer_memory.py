def remember_preferences(
    skilllayer_dir: Path,
    preferences: dict[str, dict[str, str]],
) -> dict[str, Any]:
    """Upsert preferences.md by domain. Last-writer-wins per key.

    The read + merge + write + reindex sequence runs as one transaction
    under memory_lock, so two concurrent RememberUserPreferences calls can
    never lose one call's update to the other's read-modify-write race —
    last-writer-wins is enforced by lock ordering, not by chance timing."""
    skilllayer_dir.mkdir(parents=True, exist_ok=True)
    prefs_file = skilllayer_dir / "preferences.md"

    try:
        with memory_lock(skilllayer_dir):
            # Load existing preferences
            existing: dict[str, dict[str, str]] = {}
            if prefs_file.exists():
                _, body = parse_frontmatter(prefs_file.read_text(encoding="utf-8"))
                current_domain: str | None = None
                for line in body.splitlines():
                    stripped = line.strip()
                    if stripped.startswith("## "):
                        current_domain = stripped[3:].strip().lower()
                        existing.setdefault(current_domain, {})
                    elif stripped.startswith("- ") and current_domain:
                        if ": " in stripped[2:]:
                            k, _, v = stripped[2:].partition(": ")
                            existing[current_domain][k.strip()] = v.strip()

            # Merge (last-writer-wins per key within domain)
            for domain, keys in preferences.items():
                domain_lower = domain.lower()
                existing.setdefault(domain_lower, {})
                existing[domain_lower].update(keys)

            # Serialize preferences.md
            sections_updated = sorted(existing.keys())
            body_parts: list[str] = []
            for domain in sections_updated:
                lines = [f"## {domain}"]
                for k, v in sorted(existing[domain].items()):
                    lines.append(f"- {k}: {v}")
                body_parts.append("\n".join(lines))
            body = "\n\n".join(body_parts) + "\n"

            now = _utc_iso()
            meta: dict[str, Any] = {
                "schema": SCHEMA_VERSION,
                "type": "preferences",
                "workflow": "RememberUserPreferencesWorkflow",
                "id": "preferences",
                "updated": now,
            }
            atomic_write_text(prefs_file, write_frontmatter(meta, body))
            write_index(skilllayer_dir)
    except MemoryLockTimeoutError as exc:
        return finalize_memory_result({
            "workflow": "RememberUserPreferencesWorkflow",
            "preferences_file": None,
            "sections_updated": None,
            "error_code": ERROR_MEMORY_LOCK_TIMEOUT,
            "error": str(exc),
            "summary": "No preferences updated: could not acquire the memory lock in time.",
        })
    except PermissionError as exc:
        return finalize_memory_result({
            "workflow": "RememberUserPreferencesWorkflow",
            "preferences_file": None,
            "sections_updated": None,
            "error_code": ERROR_MEMORY_PERMISSION_DENIED,
            "error": str(exc),
            "summary": "No preferences updated: permission denied writing to .skilllayer/.",
        })
    except OSError as exc:
        return finalize_memory_result({
            "workflow": "RememberUserPreferencesWorkflow",
            "preferences_file": None,
            "sections_updated": None,
            "error_code": ERROR_MEMORY_IO_ERROR,
            "error": str(exc),
            "summary": "No preferences updated: an I/O error occurred writing to .skilllayer/.",
        })

    return finalize_memory_result({
        "workflow": "RememberUserPreferencesWorkflow",
        "preferences_file": str(prefs_file),
        "sections_updated": sections_updated,
        "error_code": None,
        "error": None,
        "summary": f"Preferences updated. Sections: {', '.join(sections_updated)}.",
    })


