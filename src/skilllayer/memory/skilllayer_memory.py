def save_context_snapshot(
    skilllayer_dir: Path,
    state: str,
    open_questions: list[str],
) -> dict[str, Any]:
    """Write context/latest.md (latest-wins). Rotate previous to history/.

    The rotate + prune + write + reindex sequence runs as one transaction
    under memory_lock, so two concurrent SaveContextSnapshot calls cannot
    interleave (e.g. both rotating the same latest.md, or one reading a
    half-rotated history/ while pruning)."""
    skilllayer_dir.mkdir(parents=True, exist_ok=True)
    context_dir = skilllayer_dir / "context"
    context_dir.mkdir(exist_ok=True)
    history_dir = context_dir / "history"
    history_dir.mkdir(exist_ok=True)

    latest = context_dir / "latest.md"

    try:
        with memory_lock(skilllayer_dir):
            # Rotate existing latest to history
            if latest.exists():
                ts = _utc_filename_ts()
                history_dest = history_dir / f"{ts}.md"
                latest.rename(history_dest)

            # Prune history beyond HISTORY_KEEP
            history_files = sorted(history_dir.glob("*.md"))
            for old_file in history_files[:-HISTORY_KEEP]:
                old_file.unlink()
            history_count = len(list(history_dir.glob("*.md")))

            # Build new latest.md
            oq_lines = "\n".join(f"- {q}" for q in open_questions) if open_questions else "(none)"
            body = f"## State\n{state}\n\n## Open Questions\n{oq_lines}\n"
            now = _utc_iso()
            meta: dict[str, Any] = {
                "schema": SCHEMA_VERSION,
                "type": "context_snapshot",
                "workflow": "SaveContextSnapshotWorkflow",
                "id": "latest",
                "created": now,
                "updated": now,
            }
            atomic_write_text(latest, write_frontmatter(meta, body))
            write_index(skilllayer_dir)
    except MemoryLockTimeoutError as exc:
        return finalize_memory_result({
            "workflow": "SaveContextSnapshotWorkflow",
            "context_file": None,
            "history_count": None,
            "open_questions": open_questions,
            "error_code": ERROR_MEMORY_LOCK_TIMEOUT,
            "error": str(exc),
            "summary": "No context snapshot saved: could not acquire the memory lock in time.",
        })
    except PermissionError as exc:
        return finalize_memory_result({
            "workflow": "SaveContextSnapshotWorkflow",
            "context_file": None,
            "history_count": None,
            "open_questions": open_questions,
            "error_code": ERROR_MEMORY_PERMISSION_DENIED,
            "error": str(exc),
            "summary": "No context snapshot saved: permission denied writing to .skilllayer/.",
        })
    except OSError as exc:
        return finalize_memory_result({
            "workflow": "SaveContextSnapshotWorkflow",
            "context_file": None,
            "history_count": None,
            "open_questions": open_questions,
            "error_code": ERROR_MEMORY_IO_ERROR,
            "error": str(exc),
            "summary": "No context snapshot saved: an I/O error occurred writing to .skilllayer/.",
        })

    return finalize_memory_result({
        "workflow": "SaveContextSnapshotWorkflow",
        "context_file": str(latest),
        "history_count": history_count,
        "open_questions": open_questions,
        "error_code": None,
        "error": None,
        "summary": f"Context snapshot saved. {len(open_questions)} open question(s). {history_count} history file(s) retained.",
    })


